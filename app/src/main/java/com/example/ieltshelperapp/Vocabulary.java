package com.example.ieltshelperapp;

public class Vocabulary {
    private Integer wordId;
    private String wordENG;
    private String wordTR;
    private String wordDESC;

    public Integer getID() {
        return wordId;
    }

    public void setID(Integer wordId) {
        this.wordId = wordId;
    }

    public String getWordENG() {
        return wordENG;
    }

    public void setWordENG(String wordENG) {
        this.wordENG = wordENG;
    }

    public String getWordTR() {
        return wordTR;
    }

    public void setWordTR(String wordTR) {
        this.wordTR = wordTR;
    }
    public String getWordDESC() {
        return wordDESC;
    }

    public void setWordDESC(String wordDESC) {
        this.wordDESC = wordDESC;
    }

}
